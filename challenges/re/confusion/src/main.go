package main

import (
	"bufio"
	"fmt"
	"os"
	"strings"
)

var key = 0x25

func main() {
	reader := bufio.NewReader(os.Stdin)
	fmt.Println("This should be fun!")
	password := []byte{0x70, 0x64, 0x6d, 0x5e, 0x42, 0x15, 0x49, 0x11, 0x4b, 0x42, 0x7a, 0x48, 0x44, 0x7c, 0x7a, 0x47, 0x16, 0x7a, 0x46, 0x4a, 0x4b, 0x56, 0x14, 0x61, 0x16, 0x57, 0x16, 0x41, 0x7a, 0x51, 0x4d, 0x16, 0x7a, 0x4b, 0x16, 0x72, 0x7a, 0x75, 0x7c, 0x12, 0x6d, 0x15, 0x4b, 0x58}
	var realPassword string
	for _, c := range password {
		realPassword += string(int(c) ^ key)
	}
	fmt.Print("What is the flag?: ")
	input, _ := reader.ReadString('\n')
	input = strings.TrimSuffix(input, "\n")
	if input == realPassword {
		fmt.Println("Success!")
	}
}
